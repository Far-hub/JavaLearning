package JavaBasics;

public class StringConcatenation {

	public static void main(String[] args) {
	     // +: concatenation operator
		int i=100;
		int j=200;
		String a="Hello";
		String b="Juthi";
		System.out.println(i+j+a+b);
		System.out.println(a+b+i+j);
		System.out.println(a+b+(i+j));
		System.out.println(i+j+a+b+i+j);
		System.out.println("This is Value of i and j:"+(i+j));
		System.out.println("This is Value of a and b:" + (a+b));
		System.out.print("Having fun with Java");
		System.out.println("Best QA Tester Ever");

	}

}
