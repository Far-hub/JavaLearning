package OOPConcept;

public class MethodOverload {
	
	//main method can be overloaded with different arguments or parameters
	
     /*Method Overloading means when method name is same with different arguments 
	or input parameters within the same class*/

	public static void main(String[] args) {
	
	}
	public static void main(int x) {
		
	}
	
	//non static method can be overloaded with different arguments or parameters too.

    public void option() {
		
	}
    public void option(int x) {
		
	}

    public void option(int x, int y) {
		
	}
	
}
